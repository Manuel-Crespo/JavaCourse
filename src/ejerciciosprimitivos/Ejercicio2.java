package ejerciciosprimitivos;

public class Ejercicio2 {
    public static void main(String[] args) {

        byte varible1 = 8;
        short varible2 = 32000;
        int varible3 = 2000000000;

        long asignacion = varible1 + varible2 + varible3;
        System.out.println(asignacion);

        System.out.println("byte: " + varible1);
        System.out.println("short: " + varible2);
        System.out.println("int: " + varible3);
        System.out.println("Suma (long): " + asignacion);

    }
}
